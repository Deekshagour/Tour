 The most famous hill station in the state is Coorg where aromatic spices and coffee plantations charm tourists immensely. On the other hand, 
 Bengaluru - the IT hub of the state presents a perfect blend of software and sarees, bookshops and bars, tradition and modernity, and 
 everything in between. Just below Karnataka lies Tamil Nadu - that boasts many mesmerising sculptures and temple architecture of Chola 
 Tanjore at Chidambaram. Another city of this state that takes pride in its creative history is Madhurai. You can still hear of the 
 legendary sangrams between poets and writers that took place here almost 2000 years ago. The most magnificent city in Tamil Nadu that 
 would charm you with its Carnatic music and age-old traditions is Chennai. Though, over the years, it has acquired a cosmopolitan glamour
 with contemporary restaurants and swanky night clubs, the temples, museums and other monuments of the city are worth exploring. Southern 
 India has one of Indiaâ€™s most visited pilgrimage spots. The prominent one being the Balaji Temple at Tirupati in Andhra Pradesh. Also, the 
 ancient Buddhist site of Nagarjunakonda, and the wonderful Islamic buildings of the Qutub Shahi and Asaf Jahi dynasties at Golconda Fort 
 and Hyderabad reside here. However, a strikingly different picture is portrayed by the most loved tourist locales of Kerala and Andaman &
 Nicobar Islands. Being a symphony of evergreen forests, paddy fields, coconut palms and plantations, Kerala is a lovely world of backwaters
 that make for a perfect honeymoon destination. Here, Ayurvedic massages, houseboat cruises, wildlife sanctuaries, heritage zones and its 
 local cuisine overwhelm you. And, those seeking pristine blue waters can hit the silver soft beaches and lush greenery of Andaman and 
 Nicobar, that are the last remaining virgin rainforest islands in the world. Not to miss, Lakshadweep, an archipelago of 36 attols in 
 the south-western coast of India that has many uninhabited and untrodden islands that are even more mesmerising.with a population of over
 253 million, South India has managed to blend the cuisine and culture of its diverse ethnic groups well. The variations of its dosa, 
 sambhar, idlis and vadas have reached the far stretches of North and West regions. Known for its fresh seafood and coconut oil, it dishes
 out amazing chutneys and meat curries as well. Not to miss, the popular dance forms of Bharat Natyam, Kuchipudi, and Kathakali that found 
 their origin in this artistically rich and prospering region. </b></p> 
 
 
